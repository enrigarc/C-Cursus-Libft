#include "libft.h"

void	*ft_memchr(const void *str, int c, unsigned int n)
{
	unsigned int	i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)str;
	while (i < n)
	{
		if (ptr[i] == (unsigned char)c)
			return ((void *)str + i);
		i++;
	}
	return (NULL);
}
